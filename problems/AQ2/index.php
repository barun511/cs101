<h1>Multiply</h1>
<p>
    Note: Before reading this problem please read <a href="https://www.codechef.com/wiki/tutorial-inputoutput">this guide</a> to inputs and outputs on online judges. <br>
    Given two numbers a and b, print the value of a*b.
</p>
 <h3>Description of Input</h3><p>
    The first line of the input contains one number, x. The second line of the input contains one number, y. <br>
    </p>
 <h3>Description of Output</h3>
    <p>
    Print one line, containing one number, r, where r = x multiplied by y.
 <p>
 <h3> Constraints </h3>
 	<p>
        The values of the two numbers will be between 1 and 10^100.
 	</p>
 	<h3>Sample Input: </h3>
 	<samp>
        40 <br>
        40 <br>
 	</samp>
 	<h3> Sample Output: </h3>
 	<samp>
 	1600 <br>
 	</samp>
