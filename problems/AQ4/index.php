<h1> Quadratic </h1>
<h3> Problem Statement </h3>
<p>
    Given a quadratic equation of the form ax^2 + bx + c, determine the roots that the equation has. <br>
    If the equation has real roots, please print the two roots with a space in the middle to exactly 4 decimal places. Otherwise simply print the word "Complex". <br>
 </p>
<h3> Input Description </h3>
    The first line contains a number t, the number of test cases. <br>
    The next t lines each contain three numbers, a, b, and c, the coefficients of the quadratic equation. Each coefficient is between -100 to 100 inclusive. <br>
<h3> Output Description </h3>
    Print t lines, each containing either two real numbers with a space in the middle, or the word "Complex" (quotes are for clarity, do not print the quotes). <br>
<h3> Sample Input </h3>
<samp>
    3 <br>
    1 2 1 <br>
    -45 1 61 <br>
    59 -82 63 <br>
</samp>
<h3> Sample Output </h3>
  <samp>
    -1.0000 -1.0000   <br>
    1.1754 -1.1532 <br>
    Complex <br>
    </samp>
