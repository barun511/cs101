<h1> Mr. Eratosthenes says Hello</h1>
<h3> Problem Statement </h3>
<p>
    Given two numbers a and b, output all prime numbers between a and b inclusive. 
    <br>
    A number is considered prime if the only divisors of the number are 1 and the number itself.
 </p>
    
<h3> Input Description </h3>
    The first line contains a number t, the number of test cases. <br>
    The next t lines each contain two numbers, a and b. 
<h3> Output Description </h3>
    Output t lines.
    Each line should output the prime numbers between a and b inclusive, with a space between each number. <br>
<h3> Constraints </h3>
    Note: Constraints are <b>GUARANTEED</b> declarations of what the input will be like, you do not need to check for them. <br>
    The constraints are especially valuable in this problem.
    <br>
    1 &le; t &le; 2000<br>
    1 &le; a,b &le; 1000000<br>
    It is also guaranteed that the total number of outputs you wil need to output will not be more than 10^6.<br> 
<h3> Sample Input </h3>
<samp>
    2 <br>
    1 10 <br>
    10 20  <br>
</samp>
<h3> Sample Output </h3>
  <samp>
    2 3 5 7 <br>
    11 13 17 19<br>
    </samp>
